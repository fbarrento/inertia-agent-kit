function writeManifest(target, config) {
  const resources = [];
  const features = join(target, config.paths.features);
  if (existsSync(features)) {
    for (const name of readdirSync(features).sort()) {
      const abs = join(features, name);
      if (statSync(abs).isDirectory()) resources.push({ name, path: `${config.paths.features}/${name}` });
    }
  }
  const manifest = {
    schema: "iak.manifest.v1",
    generatedAt: now(),
    project: config.project,
    paths: config.paths,
    resources,
    conventions: config.conventions,
    commands: config.commands,
  };
  writeJson(join(target, config.paths.manifest), manifest);
  return manifest;
}

function appendGitignore(target) {
  const path = join(target, ".gitignore");
  const current = existsSync(path) ? readFileSync(path, "utf8") : "";
  const lines = [".iak/feedback/", ".iak/runs/"];
  const next = [...lines].filter((line) => !current.split(/\r?\n/).includes(line));
  if (next.length) writeFileSync(path, `${current}${current && !current.endsWith("\n") ? "\n" : ""}${next.join("\n")}\n`);
}

