function writeJson(path, value) {
  ensureDir(dirname(path));
  writeFileSync(path, `${JSON.stringify(value, null, 2)}\n`);
}

function relPath(target, path) {
  return posix(relative(target, path));
}

function kebab(input) {
  return String(input)
    .replace(/Controller$/, "")
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/[_\s]+/g, "-")
    .replace(/-+/g, "-")
    .toLowerCase();
}

function camel(input) {
  const parts = kebab(input).split("-");
  return parts[0] + parts.slice(1).map((x) => x.charAt(0).toUpperCase() + x.slice(1)).join("");
}

function pascal(input) {
  return kebab(input).split("-").map((x) => x.charAt(0).toUpperCase() + x.slice(1)).join("");
}

function singular(resource) {
  if (resource.endsWith("ies")) return `${resource.slice(0, -3)}y`;
  if (resource.endsWith("ses")) return resource.slice(0, -2);
  if (resource.endsWith("s")) return resource.slice(0, -1);
  return resource;
}

function detectAdapter(target, explicit) {
  if (explicit) return explicit;
  const pkg = readJson(join(target, "package.json")) || {};
  const deps = { ...(pkg.dependencies || {}), ...(pkg.devDependencies || {}) };
  if (deps["@inertiajs/vue3"] || deps.vue) return "vue";
  if (deps["@inertiajs/svelte"] || deps.svelte) return "svelte";
  return "react";
}

