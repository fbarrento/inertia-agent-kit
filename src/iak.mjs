function filePlan(target, files, apply) {
  const plan = { create: [], skip: [], review: [] };
  for (const file of files) {
    const abs = join(target, file.path);
    if (!existsSync(abs)) {
      plan.create.push({ path: file.path });
      if (apply) {
        ensureDir(dirname(abs));
        writeFileSync(abs, file.content);
      }
      continue;
    }
    const current = readFileSync(abs, "utf8");
    if (current === file.content) plan.skip.push({ path: file.path });
    else plan.review.push({ path: file.path });
  }
  return plan;
}

