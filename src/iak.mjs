function cmdNew(opt) {
  const kind = opt._[1];
  if (kind !== "resource") throw new Error("Usage: iak new resource <resource>");
  const resource = opt._[2];
  if (!resource) throw new Error("Usage: iak new resource <resource>");
  const config = loadConfig(opt.target);
  const files = resourceFiles(config, kebab(resource));
  const plan = filePlan(opt.target, files, opt.apply);
  if (opt.apply) {
    writeManifest(opt.target, config);
  }
  return output(opt, {
    schema: "iak.scaffold.v1",
    status: plan.review.length ? "blocked" : "completed",
    applied: opt.apply,
    resource: kebab(resource),
    plan,
  }, plan.review.length ? 1 : 0);
}

function walkFiles(root, callback) {
  let entries;
  try { entries = readdirSync(root); } catch { return; }
  for (const entry of entries) {
    if (SKIP_DIRS.has(entry)) continue;
    const full = join(root, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) walkFiles(full, callback);
    else callback(full);
  }
}

function addViolation(violations, v) {
  violations.push({
    id: `vio_${String(violations.length + 1).padStart(3, "0")}`,
    severity: "error",
    confidence: "high",
    ...v,
  });
}

function roleFor(config, rel) {
  if (rel.startsWith(`${config.paths.pages}/`)) return "page";
  if (rel.startsWith(`${config.paths.features}/`)) return "feature";
  if (rel.startsWith(`${config.paths.componentsUi}/`)) return "primitive";
  if (rel.startsWith(`${config.paths.componentsApp}/`)) return "app-component";
  if (rel.startsWith(`${config.paths.typesGenerated}/`)) return "type";
  if (rel.startsWith(`${config.paths.layouts}/`)) return "layout";
  return "unknown";
}

function scanSource(target, config, violations) {
  walkFiles(target, (file) => {
    if (!SCAN_EXT.has(extname(file))) return;
    const rel = relPath(target, file);
    const text = readFileSync(file, "utf8");
    const isToken = TOKEN_FILE.test(rel);
    text.split(/\r?\n/).forEach((line, index) => {
      for (const m of line.matchAll(ARBITRARY)) {
        addViolation(violations, {
          rule: "iak/design-system/no-arbitrary-values",
          category: "design-system",
          file: rel,
          line: index + 1,
          role: roleFor(config, rel),
          hit: m[0].trim(),
          message: "Arbitrary utility values are forbidden. Add or use a semantic token.",
        });
      }
      if (!isToken) {
        for (const m of line.matchAll(RAW_HEX)) {
          addViolation(violations, {
            rule: "iak/design-system/no-raw-hex",
            category: "design-system",
            file: rel,
            line: index + 1,
            role: roleFor(config, rel),
            hit: m[0],
            message: "Raw hex colors are forbidden outside token files.",
          });
        }
        for (const m of line.matchAll(PRIMITIVE_COLOR)) {
          addViolation(violations, {
            rule: "iak/design-system/no-primitive-color-utility",
            category: "design-system",
            file: rel,
            line: index + 1,
            role: roleFor(config, rel),
            hit: m[0].trim(),
            message: "Primitive color utilities are forbidden. Use ds semantic utilities.",
          });
        }
      }
    });

    if (roleFor(config, rel) === "page" && /\b(?:interface|type)\s+\w*PageProps\b/.test(text)) {
      addViolation(violations, {
        rule: "iak/page/no-backend-prop-types",
        category: "page",
        file: rel,
        line: 1,
        role: "page",
        hit: "PageProps",
        message: "Pages must import backend-derived page props instead of defining them locally.",
      });
    }
  });
}

function scanStructure(target, config, violations) {
  for (const name of FORBIDDEN_TOP_LEVEL) {
    const path = join(target, config.paths.root, name);
    if (existsSync(path)) {
      addViolation(violations, {
        rule: "iak/role/no-global-behavior-buckets",
        category: "role",
        file: `${config.paths.root}/${name}`,
        line: 1,
        role: "unknown",
        hit: name,
        message: `Do not create top-level ${name} folders. Keep behavior resource-local.`,
      });
    }
  }
  const actions = join(target, config.paths.root, "actions");
  if (existsSync(actions)) {
    const entries = readdirSync(actions).filter((entry) => entry !== "generated");
    if (entries.length) {
      addViolation(violations, {
        rule: "iak/role/no-handwritten-global-actions",
        category: "role",
        file: `${config.paths.root}/actions`,
        line: 1,
        role: "unknown",
        hit: entries.join(", "),
        message: "Global actions are reserved for generated Wayfinder output.",
      });
    }
  }

  const features = join(target, config.paths.features);
  if (!existsSync(features)) return;
  for (const resource of readdirSync(features)) {
    const dir = join(features, resource);
    if (!statSync(dir).isDirectory()) continue;
    const item = singular(resource);
    const typesPath = join(dir, `${item}.types.ts`);
    if (existsSync(typesPath) && !readFileSync(typesPath, "utf8").includes("@/types/generated")) {
      addViolation(violations, {
        rule: "iak/types/generated-import-required",
        category: "types",
        file: relPath(target, typesPath),
        line: 1,
        role: "feature",
        resource,
        hit: basename(typesPath),
        message: "Feature type aliases must compose generated backend types.",
      });
    }
    for (const base of [`${item}-table`, `${item}-form`]) {
      const component = join(dir, `${base}.tsx`);
      const story = join(dir, `${base}.stories.tsx`);
      if (existsSync(component) && !existsSync(story)) {
        addViolation(violations, {
          rule: "iak/stories/reusable-feature-story-required",
          category: "stories",
          file: relPath(target, component),
          line: 1,
          role: "feature",
          resource,
          hit: basename(component),
          message: "Stateful/reusable feature UI must have a colocated story.",
        });
      }
    }
  }
}

function auditProject(target, opt = {}) {
  const config = loadConfig(target);
  const violations = [];
  scanSource(target, config, violations);
  scanStructure(target, config, violations);
  const status = violations.length ? "failed" : "passed";
  const id = runId(opt);
  const result = {
    schema: "iak.audit.v1",
    runId: id,
    status,
    summary: status === "passed"
      ? "Audit passed: no IAK convention violations found."
      : `Audit failed: ${violations.length} IAK convention violation(s).`,
    checks: [
      {
        id: "iak/conventions/core",
        category: "manifest",
        status,
        severity: "error",
        violations: violations.length,
      },
    ],
    violations,
    artifacts: {
      full: { kind: "json", path: `.iak/runs/${id}/audit.json` },
    },
    nextActions: violations.map((v) => ({
      type: "fix",
      summary: v.message,
      rule: v.rule,
      file: v.file,
      line: v.line,
    })),
    errors: [],
    meta: {
      adapter: config.project.adapter,
      createdAt: now(),
    },
  };
  writeJson(join(target, ".iak/runs", id, "audit.json"), result);
  writeJson(join(target, ".iak/runs", id, "audit", "audit.json"), result);
  return result;
}

function cmdAudit(opt) {
  const result = auditProject(opt.target, opt);
  return output(opt, result, result.status === "passed" ? 0 : 1);
}

function readFeedbackRecords(target) {
  const root = join(target, ".iak/feedback");
  if (!existsSync(root)) return [];
  const records = [];
  for (const name of readdirSync(root).sort()) {
    const path = join(root, name, "record.json");
    if (existsSync(path)) {
      const record = readJson(path);
      if (record) records.push(record);
    }
  }
  return records;
}

function writeFeedback(target, record) {
  writeJson(join(target, ".iak/feedback", record.id, "record.json"), record);
}

function cmdFeedback(opt) {
  const action = opt._[1];
  if (action === "create") return feedbackCreate(opt);
  if (action === "list") return feedbackList(opt);
  if (action === "show") return feedbackShow(opt);
  if (action === "resolve") return feedbackResolve(opt);
  throw new Error("Usage: iak feedback create|list|show|resolve");
}

function feedbackCreate(opt) {
  if (!opt.message) throw new Error("Usage: iak feedback create --message <message>");
  const id = feedbackId(opt);
  const record = {
    schema: "iak.feedback.v1",
    id,
    status: "pending",
    surface: opt.surface || "app",
    source: opt.source || "human",
    producer: opt.producer || "iak.cli",
    target: {
      url: null,
      route: opt.route || null,
      storyId: null,
      selector: null,
      coordinates: null,
    },
    viewport: null,
    message: opt.message,
    tags: [],
    attachments: {
      screenshot: null,
      dom: null,
      console: null,
      network: null,
      trace: null,
    },
    context: {
      adapter: loadConfig(opt.target).project.adapter,
      componentCandidates: [],
      testRunId: null,
    },
    resolution: null,
    createdAt: now(),
    updatedAt: now(),
  };
  writeFeedback(opt.target, record);
  return output(opt, record);
}

function feedbackList(opt) {
  const status = opt.status || "pending";
  const items = readFeedbackRecords(opt.target)
    .filter((record) => !status || record.status === status)
    .map((record) => ({
      id: record.id,
      status: record.status,
      surface: record.surface,
      source: record.source,
      route: record.target.route,
      message: record.message,
      updatedAt: record.updatedAt,
    }));
  return output(opt, { schema: "iak.feedback.list.v1", status: "completed", items });
}

function feedbackShow(opt) {
  const id = opt._[2];
  if (!id) throw new Error("Usage: iak feedback show <id>");
  const record = readJson(join(opt.target, ".iak/feedback", id, "record.json"));
  if (!record) return output(opt, { schema: "iak.error.v1", status: "blocked", errors: [{ code: "feedback.not_found", message: `Feedback ${id} not found.` }] }, 2);
  return output(opt, record);
}

function feedbackResolve(opt) {
  const id = opt._[2];
  if (!id || !opt.summary || !opt.evidence) {
    throw new Error("Usage: iak feedback resolve <id> --summary <summary> --evidence <path>");
  }
  const record = readJson(join(opt.target, ".iak/feedback", id, "record.json"));
  if (!record) return output(opt, { schema: "iak.error.v1", status: "blocked", errors: [{ code: "feedback.not_found", message: `Feedback ${id} not found.` }] }, 2);
  const evidenceAbs = join(opt.target, opt.evidence);
  if (!existsSync(evidenceAbs)) {
    return output(opt, {
      schema: "iak.error.v1",
      status: "blocked",
      errors: [{ code: "feedback.evidence_missing", message: `Evidence file not found: ${opt.evidence}` }],
    }, 1);
  }
  record.status = "resolved";
  record.updatedAt = now();
  record.resolution = {
    schema: "iak.feedback.resolution.v1",
    summary: opt.summary,
    changedFiles: [],
    commandsRun: [],
    artifacts: { evidence: opt.evidence },
    resolvedAt: now(),
    resolver: { kind: "agent", id: "iak.cli" },
  };
  writeFeedback(opt.target, record);
  return output(opt, record);
}

