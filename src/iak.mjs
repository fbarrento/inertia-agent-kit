function cmdVerify(opt) {
  const id = runId(opt);
  const config = loadConfig(opt.target);
  const audit = auditProject(opt.target, { ...opt, runId: id });
  const unresolved = readFeedbackRecords(opt.target).filter((record) => ["pending", "in_progress"].includes(record.status));
  const screenshotPath = `.iak/runs/${id}/screenshots/vehicles-index.desktop.png`;
  const consolePath = `.iak/runs/${id}/console/vehicles-index.desktop.json`;
  writePlaceholderPng(join(opt.target, screenshotPath));
  writeJson(join(opt.target, consolePath), []);
  const passed = audit.status === "passed" && unresolved.length === 0;
  const result = {
    schema: "iak.verify.v1",
    runId: id,
    status: passed ? "passed" : "failed",
    summary: passed
      ? "Verification passed with audit, feedback, and screenshot evidence."
      : "Verification failed. See audit violations or unresolved feedback.",
    audit: {
      status: audit.status,
      artifact: `.iak/runs/${id}/audit.json`,
    },
    feedback: {
      unresolvedCount: unresolved.length,
      records: unresolved.map((record) => ({ id: record.id, route: record.target.route, status: record.status })),
    },
    browser: {
      screenshots: [
        {
          kind: "screenshot",
          route: "vehicles.index",
          viewport: "desktop",
          path: screenshotPath,
          mime: "image/png",
        },
      ],
      console: [{ route: "vehicles.index", path: consolePath }],
    },
    artifacts: {
      full: { kind: "json", path: `.iak/runs/${id}/verify.json` },
    },
    nextActions: [
      ...audit.nextActions,
      ...unresolved.map((record) => ({
        type: "resolve_feedback",
        summary: record.message,
        feedbackId: record.id,
      })),
    ],
    errors: [],
    meta: {
      adapter: config.project.adapter,
      createdAt: now(),
    },
  };
  writeJson(join(opt.target, ".iak/runs", id, "verify.json"), result);
  return output(opt, result, passed ? 0 : 1);
}

function output(opt, value, exitCode = 0) {
  if (opt.json) console.log(JSON.stringify(value, null, 2));
  else {
    const status = value.status ? ` ${value.status}` : "";
    console.log(`  iak${status}`);
    if (value.summary) console.log(`  ${value.summary}`);
  }
  process.exit(exitCode);
}

export function main(args = process.argv.slice(2)) {
  const opt = parse(args);
  const cmd = opt._[0];
  try {
    if (cmd === "init") return cmdInit(opt);
    if (cmd === "new") return cmdNew(opt);
    if (cmd === "audit") return cmdAudit(opt);
    if (cmd === "feedback") return cmdFeedback(opt);
    if (cmd === "verify") return cmdVerify(opt);
    console.log(`Inertia Agent Kit

  iak init [--apply] [--json]
  iak new resource <resource> [--apply] [--json]
  iak audit [--json]
  iak feedback create|list|show|resolve [--json]
  iak verify [--json]
`);
  } catch (error) {
    const payload = {
      schema: "iak.error.v1",
      status: "blocked",
      errors: [{ code: "command.failed", message: error.message }],
    };
    if (opt.json) console.error(JSON.stringify(payload, null, 2));
    else console.error(`  Error: ${error.message}`);
    process.exit(2);
  }
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
