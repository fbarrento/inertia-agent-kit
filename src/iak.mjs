function parse(args) {
  const out = { _: [], apply: false, json: process.env.IAK_AGENT === "1" };
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === "--apply" || arg === "-y") out.apply = true;
    else if (arg === "--json") out.json = true;
    else if (arg === "--target") out.target = args[++i];
    else if (arg === "--adapter") out.adapter = args[++i];
    else if (arg === "--run-id") out.runId = args[++i];
    else if (arg === "--id") out.id = args[++i];
    else if (arg === "--message") out.message = args[++i];
    else if (arg === "--route") out.route = args[++i];
    else if (arg === "--surface") out.surface = args[++i];
    else if (arg === "--source") out.source = args[++i];
    else if (arg === "--producer") out.producer = args[++i];
    else if (arg === "--status") out.status = args[++i];
    else if (arg === "--summary") out.summary = args[++i];
    else if (arg === "--evidence") out.evidence = args[++i];
    else if (arg === "--force") out.force = true;
    else out._.push(arg);
  }
  out.target ||= process.cwd();
  return out;
}

