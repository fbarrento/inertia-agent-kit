test('throws when scaffold file directories cannot be created', function (): void {
    $writeFiles = new ReflectionMethod($this->scaffolder, 'writeFiles');

    mkdir($this->basePath.'/resources/js/pages', 0755, true);
    rmdir($this->basePath.'/resources/js/pages');
    file_put_contents($this->basePath.'/resources/js/pages', 'blocked');

    set_error_handler(static fn (): bool => true);

    try {
        expect(fn () => $writeFiles->invoke($this->scaffolder, [
            [
                'path' => 'resources/js/pages/vehicles/index.tsx',
                'action' => 'create',
                'contents' => 'content',
            ],
        ]))->toThrow(RuntimeException::class, 'Unable to create directory');
    } finally {
        restore_error_handler();
    }
});

