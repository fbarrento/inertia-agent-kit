<?php

declare(strict_types=1);

use InertiaAgentKit\Scaffolding\ResourceScaffolder;
use InertiaAgentKit\Scaffolding\ResourceScaffoldOptions;
use Tests\TestCase;
use Tests\Utils\ScaffoldingTestHelper;

uses(TestCase::class);

beforeEach(function (): void {
    $this->basePath = sys_get_temp_dir().'/iak-scaffold-unit-'.bin2hex(random_bytes(6));
    mkdir($this->basePath, 0755, true);
    $this->app->setBasePath($this->basePath);
    $this->scaffolder = new ResourceScaffolder($this->app);
});

afterEach(function (): void {
    $directory = $this->basePath ?? null;

    if (! is_string($directory) || ! is_dir($directory)) {
        return;
    }

    $items = new RecursiveIteratorIterator(
        new RecursiveDirectoryIterator($directory, FilesystemIterator::SKIP_DOTS),
        RecursiveIteratorIterator::CHILD_FIRST,
    );

    foreach ($items as $item) {
        $item->isDir() ? rmdir($item->getPathname()) : unlink($item->getPathname());
    }

    rmdir($directory);
});

test('creates a completed scaffold plan and writes generated files', function (): void {
    ScaffoldingTestHelper::writeGeneratedTypes($this->basePath, 'Vehicle');

    $plan = $this->scaffolder->scaffold(new ResourceScaffoldOptions(
        resource: 'vehicles',
        adapter: 'react',
        dryRun: false,
        force: false,
        singular: null,
        only: null,
        except: null,
        allowMissingGeneratedTypes: false,
        controller: null,
        routeName: null,
        command: 'php artisan iak:make-resource vehicles',
    ));

    expect($plan['status'])->toBe('completed')
        ->and($plan['mode'])->toBe('write')
        ->and($plan['errors'])->toBe([])
        ->and($plan['writtenFiles'])->toHaveCount(12)
        ->and($plan['skippedFiles'])->toBe([])
        ->and($plan['conflicts'])->toBe([])
        ->and($plan['resource'])->toMatchArray([
            'name' => 'vehicles',
            'singular' => 'vehicle',
            'folder' => 'vehicles',
        ]);

    foreach ([
        'resources/js/pages/vehicles/index.tsx',
        'resources/js/pages/vehicles/show.tsx',
        'resources/js/pages/vehicles/create.tsx',
        'resources/js/pages/vehicles/edit.tsx',
        'resources/js/features/vehicles/vehicle.types.ts',
        'resources/js/features/vehicles/vehicle.fixtures.ts',
        'resources/js/features/vehicles/vehicle-table.tsx',
        'resources/js/features/vehicles/vehicle-table.stories.tsx',
        'resources/js/features/vehicles/vehicle-filters.tsx',
        'resources/js/features/vehicles/vehicle-form.tsx',
        'resources/js/features/vehicles/vehicle-form.stories.tsx',
        'resources/js/features/vehicles/vehicle-empty-state.tsx',
    ] as $file) {
        expect(file_exists($this->basePath.'/'.$file))->toBeTrue();
        expect(file_get_contents($this->basePath.'/'.$file))->toContain('Generated by Inertia Agent Kit. Do not edit manually.');
    }
});

